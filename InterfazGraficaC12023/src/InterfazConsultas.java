    private void jButtonEliminarXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarXActionPerformed
        String nomArch = "Empleadito.dat";
        boolean flag = false;
        ObjectInputStream arch = null; // input (lectura)
        ObjectOutputStream archAux = null; // output (escritura)

        int edad = -1;
        try {
            edad = Integer.parseInt(jTextFieldEliminarEdadX.getText());
        } catch (Exception e) {
            edad = -1;
            JOptionPane.showMessageDialog(null, "La edad debe ser un n√∫mero entero");
        }
        
        try {
            arch = new ObjectInputStream(new FileInputStream(nomArch));
            archAux = new ObjectOutputStream(new FileOutputStream("copia.dat", true));
            
            while(true){
                Empleado emp = (Empleado)arch.readObject();



                if( !(emp.getEdad() == edad) ){
                    // tengo que guardarlo
                    // se puede modfiicar el objecto
                    archAux.writeObject(emp);
                }else{
                    // lo que voy a eliminar
                    flag = true;
                }

            }

            
            
            
        } catch (Exception e) {
        }finally {
            try {
                arch.close();
            } catch (IOException ex) {
                Logger.getLogger(InterfazConsultas.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
                archAux.close();
            } catch (IOException ex) {
                Logger.getLogger(InterfazConsultas.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            if(flag != true)
                JOptionPane.showMessageDialog(null, "NO ENCONTRADO");
            else{
                JOptionPane.showMessageDialog(null, "ELIMINADO");

            }
            
            File F1 = new File(nomArch); // empleadito.dat
            File F2 = new File("copia.dat"); // auxiliar
            F1.delete();
            F2.renameTo(F1);
        }
    }//GEN-LAST:event_jButtonEliminarXActionPerformed

